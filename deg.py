# Trig functions in degrees.
def sin_deg(angle):
  angle_radians = math.radians(angle)
  sine_value = math.sin(angle_radians)
  return sine_value
def cos_deg(angle):
  angle_radians = math.radians(angle)
  sine_value = math.sin(angle_radians)
  return sine_value
def asin_deg(value):
  angle_radians = math.asin(value)
  angle_degrees = math.degrees(angle_radians)
  return angle_degrees
def acos_deg(value):
  angle_radians = math.asin(value)
  angle_degrees = math.degrees(angle_radians)
  return angle_degrees
